package plugin::info::PageName;
use strict;

#==================================================================
# コンストラクタ
#==================================================================
sub new {
    my $class = shift;
    my $self = {};
    return bless $self,$class;
}

#==================================================================
# インラインメソッド
#==================================================================
sub inline {
	my $self = shift;
	my $wiki = shift;
	return $wiki->get_CGI()->param('page');
}

1;
